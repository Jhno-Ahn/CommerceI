package user;

public class UserDataBean {
	
	private int userNum;
	private String userId;
	private String userName;
	private String userPasswd;
	private String userComNum;
	private String userLevel;
	private String userDpmt;
	private String userTel;
	private String userEmail;
	
	
	public int getUserNum() {
		return userNum;
	}
	public void setUserNum(int userNum) {
		this.userNum = userNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserPasswd() {
		return userPasswd;
	}
	public void setUserPasswd(String userPasswd) {
		this.userPasswd = userPasswd;
	}
	public String getUserComNum() {
		return userComNum;
	}
	public void setUserComNum(String userComNum) {
		this.userComNum = userComNum;
	}
	public String getUserLevel() {
		return userLevel;
	}
	public void setUserLevel(String userLevel) {
		this.userLevel = userLevel;
	}
	public String getUserDpmt() {
		return userDpmt;
	}
	public void setUserDpmt(String userDpmt) {
		this.userDpmt = userDpmt;
	}
	public String getUserTel() {
		return userTel;
	}
	public void setUserTel(String userTel) {
		this.userTel = userTel;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	
	

}
